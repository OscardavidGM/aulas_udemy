# Exercícios
# Crie funções que duplicam, triplicam e quadruplicam
# o número recebido como parâmetro.

def duplicar(numero):
    return f"{numero * 2} = el  valor doble de 500" 
def triplicar(numero):
    return f"{numero * 3} = el valor triple de 500"
def quadriplicar(numero):
    return f"{numero * 4} = el valor quadruple de 500"

dos = duplicar(500)
tres = triplicar(500)
cuatro = quadriplicar(500)

print(dos)
print(tres)
print(cuatro)
